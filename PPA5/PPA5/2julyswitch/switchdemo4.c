#include<stdio.h>

void main(){

		char ch ='B';

		switch(ch){
			
			default :
				printf("default case\n");
				break;

			case 'A' :
				printf("Case-A\n");
				break;

			case 'B' :
				printf("Case - B\n");
				break;

			case 'c':

				printf("Case-C\n");
				break;
			case 'D':
				printf("Case-D\n");
				break;

		}
}

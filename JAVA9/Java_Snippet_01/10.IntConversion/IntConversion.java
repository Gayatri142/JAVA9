


class IntConversion{
	public static void main(String[] args){
		int num = 15;

		// Integer.toBinaryString method converts int to binary
		System.out.println(Integer.toBinaryString(num));				//1111

		// Integer.toHexString method converts int to Hex
		System.out.println(Integer.toHexString(num));					//f


		// Integer.toOctalString method converts int to Octal
		System.out.println(Integer.toOctalString(num));					//17

		// Integer.toString method converts int to String Represantition
		System.out.println(Integer.toString(num));					//"15"

		// Integer.parseInt method converts String num into int
		System.out.println(Integer.parseInt("1000"));

	}
}







